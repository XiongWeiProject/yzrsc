package com.commodity.yzrsc.model;

public class TypeModel {


    /**
     * id : 1
     * name : 分类1
     * weight : 9
     */

    private int id;
    private String name;
    private int weight;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
